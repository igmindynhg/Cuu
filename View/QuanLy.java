    public NguoiDungModel getTaiKhoan() {
        String maString = lbSTT.getText();
        int maTaiKhoan = Integer.parseInt(maString);
        String tenTaiKhoan = txttkTaiKhoan.getText();
        String matKhau = txtMatKhau.getText();
        String vaiTro = txtVaiTro.getText();
        return new NguoiDungModel(tenTaiKhoan, matKhau, vaiTro, maTaiKhoan);
    }

