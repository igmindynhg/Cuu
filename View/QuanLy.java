    public final void TaiTrang(ArrayList<NguoiDungModel> arr) {
        tb.getDataVector().removeAllElements();
        for (int i = 0; i < arr.size(); i++) {
            int mataikhoan = arr.get(i).getMaTaiKhoan();
            String mataikhoanString = String.valueOf(mataikhoan);
            String nd[] = {mataikhoanString, arr.get(i).getTaiKhoan(), arr.get(i).getMatKhau(), arr.get(i).getVaiTro()};
            tb.addRow(nd);
//            jTable1.setModel(tb);
        }
        jTable3.setModel(tb);
    }

    public final void TaiTrang1(ArrayList<MenuModel> arr) {
        tb1.getDataVector().removeAllElements();
        for (int i = 0; i < arr.size(); i++) {
            int id = arr.get(i).getId();
            String Id = String.valueOf(id);
            int gia = arr.get(i).getGia();
            String Gia = String.valueOf(gia);
            String mn[] = {Id, arr.get(i).getMaMon(), arr.get(i).getTenMon(), arr.get(i).getKichCo(), Gia};
            tb1.addRow(mn);
        }
        jTable1.setModel(tb1);
    }

